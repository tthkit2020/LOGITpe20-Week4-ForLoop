using System;

namespace CountDown
{
    class Program
    {
        static void Main(string[] args)
        {
            /*The program uses for-loop to
             display numbers from 10 to 0
            including 10 and 0*/
            for(int i = 10; i >= 0; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
