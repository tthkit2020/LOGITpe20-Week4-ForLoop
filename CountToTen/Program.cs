using System;

namespace CountToTen
{
    class Program
    {
        static void Main(string[] args)
        {
            /*The program uses for-loop to
             display numbers from 0 to 10
            including 0 and 10*/
            for(int i = 0; i < 11; i++)
            {
                Console.WriteLine(i);
            }

        }
    }
}
